bool cx_util_is_power_of_2 (cxu32 n)
{
  int c = 0;
  
  if ((n & 1) == 0) // if even 
  {
    for (c = 0; n; n >>= 1)
    {
      c += (n & 1);
    }
  }
  
  return (c == 1);
}

