cx_list *cx_list_insert_front (cx_list *list, cx_list_node *node, int *count)
{
  cx_list_node *head = list;
  
  node->next = head;
  
  head = node;
  
  if (count)
  {
    ++(*count);
  }
  
  return head;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

cx_list *cx_list_remove (cx_list *list, cx_list_node *node, int *count)
{
  cx_list_node *head = list;  
  cx_list_node *prev = NULL;
  cx_list_node *curr = head;
  
  while (curr)
  {
    if (curr == node)
    {
      if (curr == head)
      {
        head = curr->next;
      }
      else 
      {
        prev->next = curr->next;
      }
      
      if (count)
      {
        --(*count);
      }
    }
    
    prev = curr;
    curr = curr->next;
  }
  
  return head;
}

