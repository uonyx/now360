typedef enum
{
  CX_FILE_MODE_READ   = 1,
  CX_FILE_MODE_WRITE  = 1 << 1,
  CX_FILE_MODE_APPEND = 1 << 2
} cx_file_mode;

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

struct cx_file
{
  FILE *fp;
  void *data;
  cxu32 size;
};

typedef struct cx_file cx_file;

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

bool cx_file_load (cx_file *file, const char *filename);
bool cx_file_unload (cx_file *file);

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

cxu32 cx_file_getsize (const char *filename);
bool cx_file_getexists (const char *filename);

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

bool cx_file_open (cx_file *file, const char *filename, cx_file_mode mode);
bool cx_file_close (cx_file *file);
cxu32 cx_file_read (cx_file *file, void *buffer, cxu32 bufferSize);
cxu32 cx_file_write (cx_file *file, void *buffer, cxu32 bufferSize);
cxi32 cx_file_flush (cx_file *file);
cxi32 cx_file_seek (cx_file *file, cxi32 offset, cxi32 origin);

