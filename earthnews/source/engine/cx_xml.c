void cx_xml_doc_create (cx_xml_doc *doc, const char *data, cxu32 dataSize)
{
  xmlDocPtr xmlDoc = xmlReadMemory (data, dataSize, "noname.xml", NULL, 0);
  
  *doc = xmlDoc;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

void cx_xml_doc_destroy (cx_xml_doc *doc)
{
  CX_ASSERT (doc);
  CX_ASSERT (*doc);
  
  xmlDocPtr xmlDoc = (xmlDocPtr) (*doc);
  
  xmlFreeDoc (xmlDoc);
  
  *doc = NULL;
}

