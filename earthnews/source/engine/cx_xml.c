////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

cx_xml_node cx_xml_doc_get_root_node (cx_xml_doc doc)
{
  CX_ASSERT (doc);
  
  xmlNodePtr rootNode = xmlDocGetRootElement (doc);
  
  return rootNode;
}

