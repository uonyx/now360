bool social_twitter_init (void)
{
  bool success = true;
  
  float version = [[[UIDevice currentDevice] systemVersion] floatValue];
  
  if (version >= 5.0f)
  {
    success = true;
  }
  else
  {
    CX_DEBUGLOG_CONSOLE (1, "failed due to pre-5.0 iOS version");
    success = false;
  }
  
  return success;
}

