typedef struct feed_news_item_t
{
  const char *title;
  const char *link;
  const char *date;
  struct feed_news_item_t *next;
} feed_news_item_t;

typedef struct feed_news_t
{
  feed_news_item_t *items;
  const char *link;
  time_t lastUpdate;
  bool dataPending;
  bool dataReady;
} feed_news_t;

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct feed_twitter_tweet_t
{
  const char *username;
  const char *userhandle;
  const char *text;
  const char *date;
  struct feed_twitter_tweet_t *next;
} feed_twitter_tweet_t;

typedef struct feed_twitter_t
{
  feed_twitter_tweet_t *items;
  unsigned int maxId;
  time_t lastUpdate;
  bool dataPending;
  bool dataReady;
} feed_twitter_t;

////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef struct feed_weather_t
{
  const char *q;
  int celsius;
  weather_condition_code conditionCode;
  const char *date;
  char time [8];
  time_t lastUpdate;
  bool dataPending;
  bool dataReady;
} feed_weather_t;

